public class User{
    String name;
    String email;
    int id;
    double weight;
    int height;
    int age;

    public User(String name, String email, int id, int age, double weight, int height){
        this.name = name;
        this.email = email;
        this.id = id;
        this.age = age;
        this.weight = weight;
        this.height = height; 
    }
}